function escapeHtml(s: string) {
  return s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

/** Minimal markdown → HTML for print (headings, lists, tables, bold, paragraphs) */
function simpleMarkdownToHtml(md: string): string {
  const lines = md.split('\n');
  const out: string[] = [];
  let inUl = false;
  let inTable = false;

  const flushUl = () => {
    if (inUl) {
      out.push('</ul>');
      inUl = false;
    }
  };
  const flushTable = () => {
    if (inTable) {
      out.push('</tbody></table>');
      inTable = false;
    }
  };

  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed) {
      flushUl();
      flushTable();
      continue;
    }
    if (/^[-*] /.test(trimmed)) {
      flushTable();
      if (!inUl) {
        out.push('<ul>');
        inUl = true;
      }
      out.push(`<li>${inline(trimmed.slice(2))}</li>`);
      continue;
    }
    if (trimmed.includes('|') && trimmed.split('|').length >= 3) {
      flushUl();
      const cells = trimmed.split('|').map((c) => c.trim()).filter(Boolean);
      if (/^[-:| ]+$/.test(trimmed)) continue;
      if (!inTable) {
        out.push('<table><thead><tr>');
        cells.forEach((c) => out.push(`<th>${inline(c)}</th>`));
        out.push('</tr></thead><tbody>');
        inTable = true;
        continue;
      }
      out.push('<tr>');
      cells.forEach((c) => out.push(`<td>${inline(c)}</td>`));
      out.push('</tr>');
      continue;
    }
    flushUl();
    flushTable();
    if (trimmed.startsWith('### ')) {
      out.push(`<h3>${inline(trimmed.slice(4))}</h3>`);
    } else if (trimmed.startsWith('## ')) {
      out.push(`<h3>${inline(trimmed.slice(3))}</h3>`);
    } else if (trimmed.startsWith('# ')) {
      out.push(`<h3>${inline(trimmed.slice(2))}</h3>`);
    } else if (trimmed.startsWith('> ')) {
      out.push(`<blockquote>${inline(trimmed.slice(2))}</blockquote>`);
    } else {
      out.push(`<p>${inline(trimmed)}</p>`);
    }
  }
  flushUl();
  flushTable();
  return out.join('\n');
}

function inline(s: string): string {
  return escapeHtml(s)
    .replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>')
    .replace(/\*(.+?)\*/g, '<em>$1</em>')
    .replace(/`(.+?)`/g, '<code>$1</code>');
}
