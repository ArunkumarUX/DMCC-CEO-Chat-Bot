import type { GeneratedDocument } from './docTypes';

/** Markdown export of generated document */
export function documentToMarkdown(doc: GeneratedDocument): string {
  const meta = [
    `# ${doc.title}`,
    '',
    `**Type:** ${doc.docType} · **Audience:** ${doc.audience} · **Style:** ${doc.style}`,
    `**Status:** ${doc.status} · **Version:** ${doc.version} · **Est. pages:** ${doc.estimatedPages}`,
    '',
    doc.summary ? `> ${doc.summary}\n` : '',
    doc.sources?.length ? `*Sources: ${doc.sources.join(' · ')}*\n` : '',
    '---',
    '',
  ].join('\n');

  const body = doc.sections
    .map((s) => `## ${s.title}\n\n${s.body.trim()}\n`)
    .join('\n---\n\n');

  return `${meta}${body}\n\n---\n*DMCC · Confidential · Generated by CEO Agent DocAI*\n`;
}

export function downloadMarkdown(doc: GeneratedDocument, filename?: string) {
  const md = documentToMarkdown(doc);
  const blob = new Blob([md], { type: 'text/markdown;charset=utf-8' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = (filename || doc.title || 'document').replace(/[^\w.-]+/g, '-').slice(0, 60) + '.md';
  a.click();
  URL.revokeObjectURL(url);
}

function safeFilename(doc: GeneratedDocument, filename?: string) {
  return (filename || doc.title || 'DMCC-document').replace(/[^\w.-]+/g, '-').slice(0, 60);
}

